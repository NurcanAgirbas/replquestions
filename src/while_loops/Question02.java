package while_loops;

public class Question02 {

	public static void main(String[] args) {
//		Create a while loop that prints out numbers from 0 to 15
//		Must include the number 0 in the output 
//		decrement by 1 example(a--)
		
		int i=15;
		while(i>=0) {
			
			System.out.println(i);
			i--;
		}

	}

}
